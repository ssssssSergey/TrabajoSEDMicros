//Callback de cuando el DMA termina (El "Timbre" de aviso)
void HAL_SPI_TxCpltCallback(SPI_HandleTypeDef *hspi) {
    if (hspi == &hspi1 && driver_pendiente != nullptr) {
        //Avisamos a LVGL de que YA hemos terminado
        lv_disp_flush_ready(driver_pendiente);
        driver_pendiente = nullptr;
    }
}

//Esta función es el "Driver de Pantalla" para LVGL
extern "C" void my_flush_cb(lv_disp_drv_t * disp_drv, const lv_area_t * area, lv_color_t * color_p)
{
    ILI9341_SetAddressWindow(area->x1, area->y1, area->x2, area->y2);

    //Guardamos el driver para avisarle luego
    driver_pendiente = disp_drv;

    //Calcular tamaño
    int32_t size = (area->x2 - area->x1 + 1) * (area->y2 - area->y1 + 1) * 2;

    // En vez de SendData (bloqueante), usamos DMA (no bloqueante)
    HAL_GPIO_WritePin(DC_GPIO_Port, DC_Pin, GPIO_PIN_SET);
    HAL_GPIO_WritePin(CS_GPIO_Port, CS_Pin, GPIO_PIN_RESET); // Bajar CS

    //El procesador lanza esto y SE VA INMEDIATAMENTE a seguir calculando el juego
    HAL_SPI_Transmit_DMA(&hspi1, (uint8_t*)color_p, size);
}



